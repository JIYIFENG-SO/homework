#include<stdio.h>
char a[105][105];
int main(void){
    int m,n,i,j,count;
    scanf("%d%d",&m,&n);
    for(i=1;i<=m;i++)
        for(j=1;j<=n;j++)
            scanf(" %c",&a[i][j]);
     for(i=1;i<=m;i++)
        for(j=1;j<=n;j++)
            if(a[i][j]!='*'){
                count=0;
                if(a[i-1][j-1]=='*'&&i!=1&&j!=1)
                    count++;
                if(a[i-1][j]=='*'&&i!=1)
                    count++;
                if(a[i-1][j+1]=='*'&&i!=1&&j!=n)
                    count++;
                if(a[i][j-1]=='*'&&j!=1)
                    count++;
                if(a[i][j+1]=='*'&&j!=n)
                    count++;
                if(a[i+1][j-1]=='*'&&i!=m&&j!=1)
                    count++;
                if(a[i+1][j]=='*'&&i!=m)
                    count++;
                if(a[i+1][j+1]=='*'&&i!=m&&j!=n)
                    count++;
                a[i][j]='0'+count;
            }
      for(i=1;i<=m;i++){
        for(j=1;j<=n;j++)
            printf("%c",a[i][j]);
            printf("\n");
    }
    return 0;
}
