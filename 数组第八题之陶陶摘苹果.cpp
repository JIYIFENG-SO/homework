using namespace std;
int main(void){
    int i,a[20],count=0,arm,max;
    for(i=0;i<10;i++)
        scanf("%d",&a[i]);
    scanf("%d",&arm);
    max=arm+30;
    for(i=0;i<10;i++)
    if(max>=a[i])
        count++;
    printf("%d",count);
    return 0;
}
